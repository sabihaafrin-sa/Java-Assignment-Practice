package Assignment_02;

public class UnaryOperator {
    public static void main(String[] args)
    {

        int num1=30, num2=20,num3;
        System.out.println("num1: "+num1);
        System.out.println("num2: "+num2);


        System.out.println("post increment of num1: "+num1++);
        System.out.println("value of num1 is: "+num1);

        System.out.println("post decrement of num2: "+num2--);
        System.out.println("value of num2 is: "+num2);

        System.out.println("pre increment of num1: "+ ++num1);
        System.out.println("value of num1 is: "+num1);

        System.out.println("pre decrement of num2: "+num2);
        System.out.println("value of num2 is: "+ --num2);


    }
}
